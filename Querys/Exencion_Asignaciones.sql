
SELECT TBBESTU_EXEMPTION_CODE, GB_COMMON.F_GET_ID(TBBESTU_PIDM), TBBESTU_TERM_CODE
    , TBBESTU_ACTIVITY_DATE, TBBESTU_TERM_CODE_EXPIRATION, TBBESTU_USER_ID
FROM TBBESTU
WHERE TBBESTU_TERM_CODE IN ('220513')
    AND TBBESTU_PIDM IN (GB_COMMON.F_GET_PIDM('N00105396')
        , GB_COMMON.F_GET_PIDM('N00135417')
        , GB_COMMON.F_GET_PIDM('N00124868')
        , GB_COMMON.F_GET_PIDM('N00085040')
        , GB_COMMON.F_GET_PIDM('N00141047'));
